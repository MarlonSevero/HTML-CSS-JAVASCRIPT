//criando variáveis
const firstValue = 50;
const secondValue = 10;
let result = 0; 

// soma
result = (firstValue + secondValue);
console.log(`${firstValue} + ${secondValue} = ${result}`);

//subtração
result = (firstValue - secondValue);
console.log(`${firstValue} - ${secondValue} = ${result}`);

// multiplicação
result = (firstValue * secondValue);
console.log(`${firstValue} * ${secondValue} = ${result}`);

// divisão
result = (firstValue / secondValue);
console.log(`${firstValue} / ${secondValue} = ${result}`);

// módulo
result = (firstValue % secondValue);
console.log(`${firstValue} % ${secondValue} = ${result}`);

// potência
result = (firstValue ** secondValue);
console.log(`${firstValue} ** ${secondValue} = ${result}`);